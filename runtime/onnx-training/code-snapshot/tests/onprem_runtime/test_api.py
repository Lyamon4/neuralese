class FakeEngine:
    def __init__(self):
        self.stopped = []
        self.cleanup_calls = []

    def list_jobs(self):
        return []

    def get_job(self, job_id):
        raise KeyError(job_id)

    def stop(self, job_id):
        self.stopped.append(job_id)
        return True

    def cleanup_jobs(self, max_age_seconds):
        self.cleanup_calls.append(max_age_seconds)
        return {
            "removed_jobs": ["job_old"],
            "removed_orphan_workspaces": ["job_orphan"],
            "retention_seconds": max_age_seconds,
        }

    async def subscribe(self, job_id):
        yield TrainingEvent(job_id=job_id, phase="completed", data={"status": "completed"})


class FakeWebSocket:
    def __init__(self, messages):
        self.messages = list(messages)

    async def receive_json(self):
        if not self.messages:
            raise WebSocketDisconnect()
        return self.messages.pop(0)


