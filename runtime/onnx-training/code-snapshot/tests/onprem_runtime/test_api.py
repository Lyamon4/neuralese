def test_dataset_sync_websocket_reuses_cached_blocks():
    sync_cache = DatasetSyncCache()
    client = TestClient(create_app(FakeEngine(), dataset_sync=sync_cache))
    payload = b"same-block"
    payload_hash = _sha(payload)
    metadata = {
        "user_id": "teacher",
        "dataset_id": "local-1",
        "header": {"rows": 1, "inputs_count": 1, "outputs_count": 0},
        "block_hashes": {"inputs": [[payload_hash]], "outputs": []},
        "hash_algo": "sha256",
    }

    with client.websocket_connect("/ws/datasets/sync") as ws:
        ws.send_json(metadata)
        assert ws.receive_json() == {"inputs": {"0": [0]}, "outputs": {}}
        ws.send_bytes(build_dataset_frame("inputs", 0, 0, payload))
        ws.send_bytes(b"__end__")
        first_summary = ws.receive_json()

    with client.websocket_connect("/ws/datasets/sync") as ws:
        ws.send_json(metadata)
        assert ws.receive_json() == {"inputs": {"0": []}, "outputs": {}}
        ws.send_bytes(b"__end__")
        second_summary = ws.receive_json()

    assert second_summary["status"] == "ok"
    assert second_summary["fingerprint"] == first_summary["fingerprint"]


def test_dataset_sync_websocket_returns_error_for_hash_mismatch():
    client = TestClient(create_app(FakeEngine(), dataset_sync=DatasetSyncCache()))

    with client.websocket_connect("/ws/datasets/sync") as ws:
        ws.send_json(
            {
                "user_id": "teacher",
                "dataset_id": "local-1",
                "header": {"rows": 1},
                "block_hashes": {"inputs": [["bad-hash"]], "outputs": []},
                "hash_algo": "sha256",
            }
        )
        assert ws.receive_json() == {"inputs": {"0": [0]}, "outputs": {}}
        ws.send_bytes(build_dataset_frame("inputs", 0, 0, b"payload"))
        ws.send_bytes(b"__end__")
        error = ws.receive_json()

    assert error["status"] == "error"
    assert "hash mismatch" in error["error"]


def _sha(payload: bytes) -> str:
    return hashlib.sha256(payload).hexdigest()
