def test_datasets_catalog_returns_public_and_cached_local_fingerprints():
    sync_cache = DatasetSyncCache()
    payload = b"encoded"
    payload_hash = _sha(payload)
    sync_cache.prepare_sync(
        "teacher",
        "local-1",
        {"rows": 1},
        {"inputs": [[payload_hash]], "outputs": []},
        hash_algo="sha256",
    )
    synced = sync_cache.apply_frames(
        "teacher",
        "local-1",
        [build_dataset_frame("inputs", 0, 0, payload)],
    )
    client = TestClient(
        create_app(
            FakeEngine(),
            dataset_sync=sync_cache,
            public_datasets={"mnist": {"name": "MNIST"}, "iris": {"name": "Iris"}},
        )
    )

    response = client.get("/api/datasets")

    assert response.status_code == 200
    assert response.json() == {
        "status": "ok",
        "public": {"mnist": {"name": "MNIST"}, "iris": {"name": "Iris"}},
        "cached_local_fingerprints": [synced.fingerprint],
    }


