def test_auth_token_protects_dataset_sync_websocket_when_configured():
    client = TestClient(
        create_app(
            FakeEngine(),
            profile=RuntimeProfile.local_school(auth_token="secret"),
            dataset_sync=DatasetSyncCache(),
        )
    )
    payload = b"encoded-input-block"
    payload_hash = _sha(payload)

    try:
        with client.websocket_connect("/ws/datasets/sync"):
            raise AssertionError("dataset sync websocket should require auth token")
    except WebSocketDisconnect as exc:
        assert exc.code == 1008

    with client.websocket_connect("/ws/datasets/sync?token=secret") as ws:
        ws.send_json(
            {
                "user_id": "teacher",
                "dataset_id": "local-1",
                "header": {"rows": 2, "inputs_count": 1, "outputs_count": 0},
                "block_hashes": {"inputs": [[payload_hash]], "outputs": []},
                "hash_algo": "sha256",
            }
        )
        assert ws.receive_json() == {"inputs": {"0": [0]}, "outputs": {}}
        ws.send_bytes(build_dataset_frame("inputs", 0, 0, payload))
        ws.send_bytes(b"__end__")
        summary = ws.receive_json()

    assert summary["status"] == "ok"


